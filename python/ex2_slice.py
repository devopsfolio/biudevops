# Создай список чисел от 1 до 10
mylist_auto = list(range(1, 11))

# Выведи первые три элемента списка с помощью среза.
print(mylist_auto[:3])

# Выведи последние пять элементов списка.
print(mylist_auto[-5:])

# Выведи элементы списка в обратном порядке с использованием среза.
print(mylist_auto[::-1])