# Аргументы функции могут иметь значения по умолчанию. 
# Это позволяет вызывать функцию без указания всех аргументов.

def greet(name, greeting="Hello"):
    print(f"{greeting}, {name}!")

greet("Alice")  # Используется значение по умолчанию
greet("Alice", "Hi")  # Переопределяем значение

# Важно: Аргументы со значением по умолчанию 
# всегда указываются после позиционных аргументов.