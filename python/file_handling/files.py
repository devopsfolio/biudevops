

# 'r' – чтение, файл должен существовать.
# 'w' – запись, создаст новый или перезапишет файл.
# 'a' – добавление в конец файла.
# 'r+', 'w+', 'a+' – комбинированные режимы для чтения и записи.
# 'b' – добавляется для работы с бинарными данными.

# Открываем файл в разных режимах и работаем с ним

# Открываем файл для чтения ('r') - если файл не существует, возникнет ошибка
with open('example.txt', 'r') as file:
    # Чтение всего содержимого файла
    content = file.read()  
    # Чтение одной строки
    line = file.readline()  
    # Чтение всех строк в виде списка
    lines = file.readlines()  

# Открываем файл для записи ('w') - создаст файл, если его нет, или перезапишет, если существует
with open('example.txt', 'w') as file:
    # Запись строки в файл
    file.write("Hello, World!\n")  
    # Запись нескольких строк из списка
    file.writelines(["Line 1\n", "Line 2\n"])  

# Открываем файл для дозаписи ('a') - данные будут добавлены в конец файла
with open('example.txt', 'a') as file:
    # Добавление строки в конец файла
    file.write("Appended line\n")  

# Открываем файл для чтения и записи ('r+') - файл должен существовать
with open('example.txt', 'r+') as file:
    # Чтение содержимого файла
    content = file.read()  
    # Переход в начало файла для записи (перезаписываем часть содержимого)
    file.seek(0)  
    file.write("Updated content\n")

# Открываем файл для записи и чтения ('w+') - создаст новый файл или перезапишет существующий
with open('example.txt', 'w+') as file:
    # Записываем данные в новый файл
    file.write("This is a new file\n")  
    # Перемещение в начало файла для чтения
    file.seek(0)  
    content = file.read()  

# Открываем файл для добавления и чтения ('a+') - создаст новый файл, если его нет
with open('example.txt', 'a+') as file:
    # Дозапись данных
    file.write("More appended content\n")  
    # Перемещение в начало файла для чтения
    file.seek(0)  
    content = file.read()  

# Режимы бинарного чтения или записи можно использовать для работы с двоичными файлами (например, изображениями)
with open('example_binary.bin', 'wb') as file:
    # Пример записи бинарных данных
    file.write(b'\x00\x01\x02\x03')  

